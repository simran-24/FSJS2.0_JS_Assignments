//Declare a variable and assign string value to it and change all the string characters to capital letters using toUpperCase() method.

let lowerstring = "i am lowercase and will convert into uppercase.";
console.log("Before using toUpperCase() method : " + lowerstring);

let upperstring = lowerstring.toUpperCase();
console.log("After using toUpperCase() method : " +upperstring);


//OUTPUT Will be :- 
// Before using toUpperCase() method : i am lowercase and will convert into uppercase.
// After using toUpperCase() method :  I AM LOWERCASE AND WILL CONVERT INTO UPPERCASE.